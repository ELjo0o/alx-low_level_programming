#include <stdio.h>
#include "main.h"


/**
*reset_to_98 - function that takes a pointer to an int as parameter
*and updates the value it points to to 98.
*@n: number of pointer.
*Return: (0).
*/


void reset_to_98(int *n)
{
	int *n;

	if (*n == 98)
	{
		_putchar("%d\n", *n);
	}
	else
	{
		_putchar("%d\n", *n);
	}
	_putchar('\n');
	return (0);
}
